    @Override
    public void paint(Graphics g) {
        super.paint(g); //To change body of generated methods, choose Tools | Templates.
        if(mPattern != null && mPattern.length > 1) {
            g.setColor(Color.RED);
            double width = getWidth();
            double height = getHeight();
            width = width/3;
            height = height/3;
            int paddingX = (int) (width/2);
            int paddingY = (int) (height/2);

            int x1,y1,x2,y2;
            int[] coordinates = COORDINATES[mPattern[0]];
            x1 = paddingX + (int) ((coordinates[0] - 1) * width);
            y1 = paddingY + (int) ((coordinates[1] - 1) * height);
       
            for(int i=1;i<mPattern.length;i++) {
                coordinates = COORDINATES[mPattern[i]];
                x2 = paddingX + (int) ((coordinates[0] - 1) * width);
                y2 = paddingY + (int) ((coordinates[1] - 1) * height);
                g.drawLine(x1, y1, x2, y2);
                x1 = x2;
                y1 = y2;
            }
        }
    }
    
    
    
    

