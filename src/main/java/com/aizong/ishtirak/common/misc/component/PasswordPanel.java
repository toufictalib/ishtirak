    public void setPattern(String pattern) {
        if(pattern.contains("0")) {
            mPattern = null;
        }
        else {
            mPattern = new int[pattern.length()];
            try {
                for(int i=0;i<pattern.length();i++) {
                    mPattern[i] = Integer.parseInt(pattern.substring(i, i+1)) - 1;
                }
            } catch(Exception ex) {
                ex.printStackTrace();
                mPattern = null;
            }
        }
        repaint();
    }

